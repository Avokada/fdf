#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	char *dest;

	dest = s1;
	while (*dest != '\0')
		dest++;
	while (*s2 != '\0')
		*dest++ = *s2++;
	*dest = '\0';
	return (s1);
}
